<?php namespace Qdesign\Services\Form\Gallery;

use Qdesign\Services\Validation\ValidableInterface;
use Qdesign\Repositories\Gallery\GalleryInterface;

class GalleryForm {

    /**
     * Form Data
     *
     * @var array
     */
    protected $data;


    /**
     * @var \Qdesign\Services\Validation\ValidableInterface
     */
    protected $validator;


    /**
     * @var \Qdesign\Repositories\Gallery\GalleryInterface
     */
    protected $gallery;

    public function __construct(ValidableInterface $validator, GalleryInterface $gallery)
    {
        $this->validator = $validator;
        $this->gallery = $gallery;
    }

    /**
     * @param array $input
     * @return bool
     */
    public function save(array $input)
    {
        if( ! $this->valid($input) )
        {
            return false;
        }
        return $this->gallery->create($input);
    }


    /**
     * @param array $input
     * @return bool
     */
    public function update(array $input)
    {
        if( ! $this->valid($input) )
        {
            return false;
        }

        return $this->gallery->update($input);
    }

    /**
     * Return any validation errors
     *
     * @return array
     */
    public function errors()
    {
        return $this->validator->errors();
    }

    /**
     * @param array $input
     * @return mixed
     */
    protected function valid(array $input)
    {
        return $this->validator->with($input)->passes();
    }
}